package assignment;

public class Atozdisplay {

	public static void main(String[] args) {
		
		System.out.println("\nUpper character letter ");
		for(char ch='A'; ch<='Z';ch++) {
			
			
			
			System.out.print(ch+" ");
			
			
		}
		
		
		System.out.println("\nLower character letter ");
		
		for(char ch='b'; ch<='z';ch++) {
			
			
		
			System.out.print(ch+" ");
			
		}
		

	}

}
